In the cause of development and reaching out to lives of the poor, we believe in in CHANGE. We believe in the idea that CHANGE is possible in our state. Hope, is not a blind optimism, is not a wishful thinking, but hope in the face of difficulty, hope in the face of uncertainty. That faith in the future has pushed us not to believe but to understand change, even when the odds are great. We’ve seen the penetration of insecurity in our State, but not to worry, because change is here and change is always now. My parents not only shared an improbable love, they shared an abiding fate in the possibilities of this nation. 

